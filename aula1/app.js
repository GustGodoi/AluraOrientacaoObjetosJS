// conceitos de OO (orientação a objetos)
// padrões de programação boaseados em teorias matemáticas e computacionais
// a própria Orientação a objetos é um padrão de desenvolvimento baseado no paradigma imperativo

// A orientação a objetos é o princípio de espelhar as caracteristicas do mundo
// através de estuturas com caracteristicas e ações correlacionais
// Aproxima o sistema da realidade, através da modelagem dele a realidade

// Objetos são diferenciados em caracteristicas e comportamentos
// Ou seja, seua padrãos, qualidades e valores | E oq esse objeto faz, quais as ações dele

// metodos são funções executadas por um objeto
// elementos são suas caracteristicas

// a base da orientação a objetos, é trazer pra dentro do código, modelos de objetos que representem a realidade

// 5 princípios da orientação a Objetos:
// - Classe;
// - Objeto;
// - herança;
// - Polimorfismo;
// - Encapsulamento

